package com.muzongyan.maven.archetypes.wx.dtos;

/**
 * @author muzongyan
 *
 */
public class WxCpConfig {

    private String corp_id;

    private String agent_id;

    private String corp_secret;

    private String token;

    private String aes_key;

    private String access_token;

    private int expires_in;

    private String http_proxy_host;

    private int http_proxy_port;

    private String http_proxy_username;

    private String http_proxy_password;

    /**
     * @return the corp_id
     */
    public String getCorp_id() {
        return corp_id;
    }

    /**
     * @param corp_id
     *            the corp_id to set
     */
    public void setCorp_id(String corp_id) {
        this.corp_id = corp_id;
    }

    /**
     * @return the agent_id
     */
    public String getAgent_id() {
        return agent_id;
    }

    /**
     * @param agent_id
     *            the agent_id to set
     */
    public void setAgent_id(String agent_id) {
        this.agent_id = agent_id;
    }

    /**
     * @return the corp_secret
     */
    public String getCorp_secret() {
        return corp_secret;
    }

    /**
     * @param corp_secret
     *            the corp_secret to set
     */
    public void setCorp_secret(String corp_secret) {
        this.corp_secret = corp_secret;
    }

    /**
     * @return the token
     */
    public String getToken() {
        return token;
    }

    /**
     * @param token
     *            the token to set
     */
    public void setToken(String token) {
        this.token = token;
    }

    /**
     * @return the aes_key
     */
    public String getAes_key() {
        return aes_key;
    }

    /**
     * @param aes_key
     *            the aes_key to set
     */
    public void setAes_key(String aes_key) {
        this.aes_key = aes_key;
    }

    /**
     * @return the access_token
     */
    public String getAccess_token() {
        return access_token;
    }

    /**
     * @param access_token
     *            the access_token to set
     */
    public void setAccess_token(String access_token) {
        this.access_token = access_token;
    }

    /**
     * @return the expires_in
     */
    public int getExpires_in() {
        return expires_in;
    }

    /**
     * @param expires_in
     *            the expires_in to set
     */
    public void setExpires_in(int expires_in) {
        this.expires_in = expires_in;
    }

    /**
     * @return the http_proxy_host
     */
    public String getHttp_proxy_host() {
        return http_proxy_host;
    }

    /**
     * @param http_proxy_host
     *            the http_proxy_host to set
     */
    public void setHttp_proxy_host(String http_proxy_host) {
        this.http_proxy_host = http_proxy_host;
    }

    /**
     * @return the http_proxy_port
     */
    public int getHttp_proxy_port() {
        return http_proxy_port;
    }

    /**
     * @param http_proxy_port
     *            the http_proxy_port to set
     */
    public void setHttp_proxy_port(int http_proxy_port) {
        this.http_proxy_port = http_proxy_port;
    }

    /**
     * @return the http_proxy_username
     */
    public String getHttp_proxy_username() {
        return http_proxy_username;
    }

    /**
     * @param http_proxy_username
     *            the http_proxy_username to set
     */
    public void setHttp_proxy_username(String http_proxy_username) {
        this.http_proxy_username = http_proxy_username;
    }

    /**
     * @return the http_proxy_password
     */
    public String getHttp_proxy_password() {
        return http_proxy_password;
    }

    /**
     * @param http_proxy_password
     *            the http_proxy_password to set
     */
    public void setHttp_proxy_password(String http_proxy_password) {
        this.http_proxy_password = http_proxy_password;
    }

}
